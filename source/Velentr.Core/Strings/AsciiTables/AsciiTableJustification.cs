namespace Velentr.Core.Strings.AsciiTables;

/// <summary>
///     Specifies the justification for text in an ASCII table column.
/// </summary>
public enum AsciiTableJustification
{
    /// <summary>
    ///     Text is left-justified.
    /// </summary>
    Left = 1,

    /// <summary>
    ///     Text is right-justified.
    /// </summary>
    Right = 2,

    /// <summary>
    ///     Text is center-justified.
    /// </summary>
    Center = 3
}
