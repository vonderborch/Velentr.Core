namespace Velentr.Core.UnitConversions.ComputerBits.Bytes;

/// <summary>
/// Represents the different scales of bytes.
/// </summary>
public enum ByteScale
{
    /// <summary>
    /// Represents a byte.
    /// </summary>
    Byte,

    /// <summary>
    /// Represents a kilobyte.
    /// </summary>
    KiloByte,

    /// <summary>
    /// Represents a megabyte.
    /// </summary>
    MegaByte,

    /// <summary>
    /// Represents a gigabyte.
    /// </summary>
    GigaByte,

    /// <summary>
    /// Represents a terabyte.
    /// </summary>
    TeraByte,

    /// <summary>
    /// Represents a petabyte.
    /// </summary>
    PetaByte,

    /// <summary>
    /// Represents an exabyte.
    /// </summary>
    ExaByte,
}
