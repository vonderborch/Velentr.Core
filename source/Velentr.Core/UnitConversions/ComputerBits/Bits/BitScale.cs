namespace Velentr.Core.UnitConversions.ComputerBits.Bits;

/// <summary>
/// Represents the scale of computer bits.
/// </summary>
public enum BitScale
{
    /// <summary>
    /// Represents a single bit.
    /// </summary>
    Bit,
    
    /// <summary>
    /// Represents a kilobit (1,000 bits).
    /// </summary>
    KiloBit,
    
    /// <summary>
    /// Represents a megabit (1,000,000 bits).
    /// </summary>
    MegaBit,
    
    /// <summary>
    /// Represents a gigabit (1,000,000,000 bits).
    /// </summary>
    GigaBit,
    
    /// <summary>
    /// Represents a terabit (1,000,000,000,000 bits).
    /// </summary>
    TeraBit,
    
    /// <summary>
    /// Represents a petabit (1,000,000,000,000,000 bits).
    /// </summary>
    PetaBit,
    
    /// <summary>
    /// Represents an exabit (1,000,000,000,000,000,000 bits).
    /// </summary>
    ExaBit,
}
